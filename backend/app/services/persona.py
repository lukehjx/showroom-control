# ==================== 获取话术 ====================

def get_reply(key: str, **kwargs) -> str:
    """获取随机话术，支持format参数替换"""
    replies = REPLIES.get(key)
    if replies is None:
        return ""
    if isinstance(replies, list):
        text = random.choice(replies)
    elif isinstance(replies, dict):
        # 子分类，返回第一个匹配或默认
        text = random.choice(list(replies.values())[0])
    else:
        text = str(replies)

    # 替换参数
    if kwargs:
        try:
            text = text.format(**kwargs)
        except Exception:
            pass
    return text


def get_arrive_reply(poi: str) -> str:
    """获取到达点位的话术"""
    poi_replies = REPLIES["arrive_at"]
    replies = poi_replies.get(poi, poi_replies["default"])
    return random.choice(replies)


def get_time_greeting() -> str:
    """根据时间返回问候语"""
    hour = datetime.now().hour
    if 6 <= hour < 12:
        return "早上好！"
    elif 12 <= hour < 14:
        return "中午好！"
    elif 14 <= hour < 18:
        return "下午好！"
    elif 18 <= hour < 22:
        return "晚上好！"
    else:
        return "您好！"


def build_speak_params(text: str, scene: str = "normal") -> dict:
    """构造TTS参数（语速根据场景调整）"""
    speeds = {
        "welcome": 1.0,     # 欢迎语，标准语速
        "explain": 0.9,     # 讲解，稍慢清晰
        "navigation": 1.1,  # 过渡，轻快
        "emergency": 1.2,   # 紧急提示，快
        "normal": 1.0,
    }
    return {
        "text": text,
        "speed": speeds.get(scene, 1.0),
    }
